import {Component, OnInit, ViewChild} from '@angular/core';
import {MatTableDataSource} from "@angular/material/table";
import {MatPaginator} from "@angular/material/paginator";
import {DataService} from "../../services/data.service";
import {MatSort} from "@angular/material/sort";
import {catchError, merge, startWith, switchMap, Observable, of, map} from "rxjs";
import {Request} from "../../types/request";

@Component({
  selector: 'admin-requests-table',
  templateUrl: './requests-table.component.html',
  styleUrls: ['./requests-table.component.scss']
})
export class RequestsTableComponent implements OnInit {

  displayedColumns: string[] = ['id', 'address', 'client_name', 'phone', 'type', 'worker_name', 'status'];
  data: Request[] = [];

  resultsLength = 0;
  isLoadingResults = true;
  isRateLimitReached = false;

  @ViewChild(MatPaginator) paginator!: MatPaginator;
  @ViewChild(MatSort) sort!: MatSort;

  constructor(
    private dataService: DataService
  ) { }

  ngAfterViewInit() {
    // If the user changes the sort order, reset back to the first page.
    this.sort.sortChange.subscribe(() => (this.paginator.pageIndex = 0));

    merge(this.sort.sortChange, this.paginator.page)
      .pipe(
        startWith({}),
        switchMap(() => {
          this.isLoadingResults = true;
          return this.dataService.getRequests(
            this.paginator.pageIndex,
          ).pipe(catchError(() => of(null)));
        }),
        map(data => {
          // Flip flag to show that loading has finished.
          this.isLoadingResults = false;
          this.isRateLimitReached = data === null;

          if (data === null) {
            return [];
          }

          // Only refresh the result length if there is new data. In case of rate
          // limit errors, we do not want to reset the paginator to zero, as that
          // would prevent users from re-triggering requests.
          this.resultsLength = data.count;
          return data.rows;
        }),
      ).subscribe(data => (this.data = data));
  }

  ngOnInit(): void {
  }

}


export interface RequestElement {
  id: number;
  address: string;
  client_id: number;
  type_id: number;
  worker_id: number;
  status_id: number;
  datetimeComplite: string;
}

